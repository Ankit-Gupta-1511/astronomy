function IsValidRotationArray(rot) {
    if (!(rot instanceof Array) || (rot.length !== 3))
        return false;

    for (let i=0; i < 3; ++i) {
        if (!(rot[i] instanceof Array) || (rot[i].length !== 3))
            return false;

        for (let j=0; j < 3; ++j)
            if (typeof rot[i][j] !== 'number')
                return false;
    }

    return true;
}


